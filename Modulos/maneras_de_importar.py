# Para importar módulos en Python, se usa la instrucción import. Por ejemplo, para importar el módulo math, se escribiría:

# import math

# También puedes importar módulos con un alias usando la siguiente sintaxis:

# import module_name as alias
# Ej:

# import math as m

# Además, puedes importar funciones específicas de un módulo usando la siguiente sintaxis:

# from module_name import function_name

# Ej:

# from math import sqrt


# Finalmente, puedes importar todas las funciones de un módulo usando la siguiente sintaxis:
# No se recomienda esta practica

# from module_name import *

# Ej:

# from math import *
